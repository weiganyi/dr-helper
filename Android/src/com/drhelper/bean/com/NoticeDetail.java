package com.drhelper.bean.com;

public class NoticeDetail {
	private String event;
	private boolean emptyTable;
	private boolean finishMenu;
	private int table;

	public String getEvent() {
		return event;
	}
	public void setEvent(String event) {
		this.event = event;
	}
	public boolean isEmptyTable() {
		return emptyTable;
	}
	public void setEmptyTable(boolean emptyTable) {
		this.emptyTable = emptyTable;
	}
	public boolean isFinishMenu() {
		return finishMenu;
	}
	public void setFinishMenu(boolean finishMenu) {
		this.finishMenu = finishMenu;
	}
	public int getTable() {
		return table;
	}
	public void setTable(int table) {
		this.table = table;
	}
}
