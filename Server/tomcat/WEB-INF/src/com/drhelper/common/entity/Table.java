package com.drhelper.common.entity;

public class Table {
	private int table_id;
	private int table_num;
	private int table_seat_num;
	private int table_empty;

	public int getTable_id() {
		return table_id;
	}
	public void setTable_id(int table_id) {
		this.table_id = table_id;
	}
	public int getTable_num() {
		return table_num;
	}
	public void setTable_num(int table_num) {
		this.table_num = table_num;
	}
	public int getTable_seat_num() {
		return table_seat_num;
	}
	public void setTable_seat_num(int table_seat_num) {
		this.table_seat_num = table_seat_num;
	}
	public int getTable_empty() {
		return table_empty;
	}
	public void setTable_empty(int table_empty) {
		this.table_empty = table_empty;
	}
}
