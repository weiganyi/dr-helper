package com.drhelper.android.bean.com;

public class NoticeHeartBeat {
	private String msg;
	private boolean result;

	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public boolean isResult() {
		return result;
	}
	public void setResult(boolean result) {
		this.result = result;
	}
}
